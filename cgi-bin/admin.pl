sub list_admin
{
 $colspan=9;
 $header=&RRow('tablebg','c3ccccc','Логин','Редактировать','[X]','Имя','Должность','Разрешен','Суперадмин?');
 $old_office=0;
 $out='';
 $outleft='';
 $sth=&sql($dbh,"SELECT * FROM admin ORDER BY office,admin");
 while ($p=$sth->fetchrow_hashref)
   {
    ($office,$admin,$privil,$id)=&Get_fields('office','admin','privil','id');
    if ($office!=$old_office && $office)
      {
       $out.=&RRow('head','c'.($colspan-1),&ahref('#top','&uarr;',"name=$office")," Отдел <b>$Offices{$office}</b>").$header;
       $outleft.=&ahref("#$office",$Offices{$office});
      } 
    $old_office=$office;
    %pr=();
    $pr{$_}=1 foreach (split /,/,$privil);
    $enabled=$pr{1}? '' : 'отключен';
    $super=$pr{3} && $pr{5}? '<span class=error>Суперадмин</span>' : $pr{2}? 'Повышенные привилегии' : '&nbsp;';
    ($name,$post)=&Get_filtr_fields('name','post');
    $out.=&RRow($pr{1}? '*': 'rowoff','llllllccc',
      &bold($admin),
      &ahref("$scrpt&act=edit_priv&id=$id",'Привилегии'),
      &ahref("$scrpt&act=edit_data&id=$id",'Данные'),
      &ahref("$scrpt&act=copy_data&id=$id",'Копия'),
      &ahref("$scrpt&act=del_admin&id=$id",'Х'),
      "<div nowrap>$name</div>",
      $post,
      $enabled,
      $super
    );
   }
 $out or &Error('Не создана ни одна учетная запись администратора. '.$br2.&CenterA("$scrpt&act=new_admin",'Создать &rarr;'));
 $OUT.=&Table('width100 nav2',
   &RRow('','tt',
     $outleft && &MessX($outleft),
     &Table('tbg3',&RRow('head',$colspan,&bold('Список администраторов')).$out)
   )
 );
}

