sub update_data
{
 &get_admin_data;
 $oldpasswd=$p->{"AES_DECRYPT(passwd,'$Passwd_Key')"};

 &check_admin_login($F{admin});

 $p=&sql_select_line($dbh,"SELECT * FROM admin WHERE admin='$Fadmin' AND id<>$id");
 $p && &Error('Запись с логином '.&bold($Fadmin).' уже существует!');

 $Fpasswd=&trim(&Filtr_mysql($F{passwd}));
 $Fname=&Filtr_mysql($F{name});

 $Fpost=&Filtr_mysql($F{post});

 $Foffice=int $F{office};
 $rows=$dbh->do("UPDATE admin SET admin='$Fadmin',passwd=AES_ENCRYPT('$Fpasswd','$Passwd_Key'),name='$Fname',post='$Fpost',office=$Foffice WHERE id=$id LIMIT 1");
 $rows<1 && &Error("Произошла ошибка при выполнении sql-запроса. Данные администратора не изменены.");
 &ToLog("! $Admin_UU Изменены данные администратора $admin.".
   ($admin ne $Fadmin && " Новый логин $Fadmin.").
   ($oldpasswd ne $F{passwd} && ' Изменен пароль.').
   ($office!=$Foffice && ' Отдел: '.($Offices{$Foffice} || ' не установлен'))
 );

 # к каким группам разрешить доступ
 $sth=&sql($dbh,"SELECT * FROM user_grp");
 while ($p=$sth->fetchrow_hashref)
   {
    $grp_id=$p->{grp_id};
    $g1=$p->{grp_admins};
    $g2=$p->{grp_admins2};
    $g1=~s|,$id,|,|;
    $g2=~s|,$id,|,|;
    $g1=~s|0$||;
    $g2=~s|0$||;
    $g1.="$id," if $F{"g$grp_id"} || $F{"gg$grp_id"};
    $g2.="$id," if $F{"g$grp_id"} && $F{"gg$grp_id"};
    $g1.='0';
    $g2.='0';
    &sql_do($dbh,"UPDATE user_grp SET grp_admins='$g1',grp_admins2='$g2' WHERE grp_id=$grp_id LIMIT 1");
   }

 $mess='';
 # Если пароль менялся - удалим все активные сессии данного админа
 if ($oldpasswd ne $F{passwd})
   {
    $rows=&sql_do($dbh,"DELETE FROM admin_session WHERE admin_id=$id");
    $rows>0 && ($mess=$br.'Удалены активные сессии данного администратора т.к. вы изменили пароль его учетной записи');
   } 

 &OkMess(&show_admin_url('Изменены данные администратора '.&bold($Fadmin),$id).$mess);
}

1;
