sub save_new
{
 &check_admin_login($F{admin});

 $Fpasswd=&trim(&Filtr_mysql($F{passwd}));
 $p=&sql_select_line($dbh,"SELECT admin FROM admin WHERE admin='$Fadmin' LIMIT 1");
 # обрати внимание, что даже если кто-то параллельно успеет вставить запись с таким логином, то INSERT не выполнится из-за уникальности поля
 $p && &Error('Админ с логином '.&bold($Fadmin).' уже существует.'.$br2.
    'Учетная запись администратора не создана.'.$br2.
    &CenterA("$scrpt&act=list_admin",'Список администраторов'));
 $sth=$dbh->prepare("INSERT INTO admin SET admin='$Fadmin',passwd=AES_ENCRYPT('$Fpasswd','$Passwd_Key')");
 $sth->execute;
 $id=$sth->{mysql_insertid} || $sth->{insertid};
 &Error("Ошибка при выполнении sql-запроса создания учетной записи $Fadmin. Возможно администратор с таким логином уже существует.") unless $id;
 &ToLog("!! $Admin_UU Создан новый администратор с логином $Fadmin.");
 &OkMess(&show_admin_url("Создана учетная запись администратора с логином $Fadmin",$id));
}

